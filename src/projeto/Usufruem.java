package projeto;

public class Usufruem {
	private int id_festa;
	private int id_aniversariante;
	protected Usufruem(int id_festa, int id_aniversariante) {
		super();
		this.id_festa = id_festa;
		this.id_festa = id_festa;
	}
	protected int getId_festa() {
		return id_festa;
	}
	protected void setId_festa(int id_festa) {
		this.id_festa = id_festa;
	}
	protected int getId_aniversariante() {
		return id_aniversariante;
	}
	protected void setId_aniversariante(int id_aniversariante ) {
		this.id_aniversariante = id_aniversariante;
	}
	
}
